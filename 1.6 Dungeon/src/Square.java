
public class Square {
    private int x;
    private int y;
    private char name;
    
    
    public Square(int x, int y, char name) {
	super();
	this.x = x;
	this.y = y;
	this.name = name;
    }


    public int getX() {
        return x;
    }


    public void setX(int x) {
        this.x = x;
    }


    public int getY() {
        return y;
    }


    public void setY(int y) {
        this.y = y;
    }


    public char getName() {
        return name;
    }


    public void setName(char name) {
        this.name = name;
    }
    
    
    
}
